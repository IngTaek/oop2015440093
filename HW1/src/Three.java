import java.util.Scanner;

public class Three {

	public static void main(String[] args) {
		// 3. 세 수(정수형)를 입력 받아 가장 큰 수를 출력하는 프로그램을 작성하시오.
		
		int x, y, z, compare1, compare2;
		
		Scanner input = new Scanner(System.in);
		
		System.out.print("첫번째 정수를 입력하세요 : ");
		x = input.nextInt(); // 키보드에 숫자 입력
		
		System.out.print("두번째 정수를 입력하세요 : ");
		y = input.nextInt(); // 키보드에 숫자 입력
		
		System.out.print("세번째 정수를 입력하세요 : ");
		z = input.nextInt(); // 키보드에 숫자 입력
		
		if(x >= y)
		{
			compare1 = x;
		}
		else
		{
			compare1 = y;
		}
		
		if(compare1 >= z)
		{
			compare2 = compare1;
		}
		else
		{
			compare2 = z;
		}
		
		System.out.println("가장 큰 수는 " + compare2 + "입니다.");


		

		

	}

}
